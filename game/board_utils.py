def get_card_str(card_id):
    """Returns a list of 5 strings representing the card's move pattern."""
    if card_id is None: return ["     "] * 5
    
    offsets = ALL_CARDS[card_id]["pattern"] 
    lines = []
    for r in range(5):
        line = ""
        for c in range(5):
            dr, dc = r - 2, c - 2
            
            if dr == 0 and dc == 0:
                line += " @ " 
            elif (dr, dc) in offsets:
                line += " X " 
            else:
                line += " . "
        lines.append(line)
    return "\n".join(lines)


